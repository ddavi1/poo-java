package aula3;

public class Aluno {

    private String nome;
    private int idade;

    // métodos apenas para retorno
    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    // métodos apenas para retorno
    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }
}
